age = 16
if age >= 18:
    print("You are old enough to vote!")
    print("Have you registered to vote yet?")
else:
    print("Sorry, you are too young to vote")
    print("Please register to vote when you turn 18.")

# This is a demonstration of an if-else statement in Python. An if-else statement allows the user to define an action if the conditional test fails. In this case, because the age is 16, and 16 is not greater than or equal to 18, the statements in the else portion print instead.