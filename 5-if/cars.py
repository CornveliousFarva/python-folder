cars = ['audi', 'bmw', 'subaru', 'toyota']

for car in cars:
    if car == 'bmw':
        print(car.upper())
    else:
        print(car.title())

# Differences in equals signs

# A single equals sign (=) means to set the value at an item, so if you were to say car = 'bmw', it would be setting the value.

# A double equals sign (==) is asking a question: is the car value equal to bmw?