age = 23
if age >= 18:
    print("You are old enough to vote!")


# At its most basic form, if statements work like this:
# if condition:
#     do something
# In the above example, the age is greater than 18, so they're old enough to vote.
# >= means greater than or equal to. So if you were to put in 18, you would get the same message if you put in a number greater than 18