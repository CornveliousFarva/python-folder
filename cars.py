# Sorting a list
cars = ['bwm', 'audi', 'volkswagen', 'subaru']
cars.sort()
print(cars)

# Sorting a list but in reverse
cars = ['bwm', 'audi', 'volkswagen', 'subaru']
cars.sort(reverse=True)
print(cars)

# Sorting a list temporarily with the sorted() functiob
cars = ['bwm', 'audi', 'volkswagen', 'subaru']

print("Here is the original list")
print(cars)

print("\nHere is the original list:")
print(sorted(cars))

print("\nHere is the original list again")
print(cars)

# Printing a list in reverse 
cars = ['bwm', 'audi', 'volkswagen', 'subaru']
cars.reverse()
print(cars)

